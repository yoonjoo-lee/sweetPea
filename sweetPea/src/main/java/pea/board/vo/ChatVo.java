package pea.board.vo;


public class ChatVo {

	private int chidx;
	private int uidx;
	private String content;
	private String date;
	private int youidx;
	
	
	public int getChidx() {
		return chidx;
	}
	public void setChidx(int chidx) {
		this.chidx = chidx;
	}
	public int getUidx() {
		return uidx;
	}
	public void setUidx(int uidx) {
		this.uidx = uidx;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getYouidx() {
		return youidx;
	}
	public void setYouidx(int youidx) {
		this.youidx = youidx;
	}
	
	
	
	
}
