package pea.board.vo;

public class ReportVo {

	
	private int ridx;
	private int bidx;
	private int reply;
	private String troll;
	private int report;
	private int rbidx;
	private int rcidx;
	
	
	
	public int getRidx() {
		return ridx;
	}
	public void setRidx(int ridx) {
		this.ridx = ridx;
	}
	public int getBidx() {
		return bidx;
	}
	public void setBidx(int bidx) {
		this.bidx = bidx;
	}
	public int getReply() {
		return reply;
	}
	public void setReply(int reply) {
		this.reply = reply;
	}
	public String getTroll() {
		return troll;
	}
	public void setTroll(String troll) {
		this.troll = troll;
	}
	public int getReport() {
		return report;
	}
	public void setReport(int report) {
		this.report = report;
	}
	public int getRbidx() {
		return rbidx;
	}
	public void setRbidx(int rbidx) {
		this.rbidx = rbidx;
	}
	public int getRcidx() {
		return rcidx;
	}
	public void setRcidx(int rcidx) {
		this.rcidx = rcidx;
	}
	
	
	
}
