package pea.board.vo;

public class MiniCommentVo {
	
	
	private int mcidx;
	private int uidx;
	private String content;
	private String writer;
	private String ip;
	private int level_;
	private int depth;
	private int originmcidx;
	private String delyn;
	private String date;
	
	
	
	public int getMcidx() {
		return mcidx;
	}
	public void setMcidx(int mcidx) {
		this.mcidx = mcidx;
	}
	public int getUidx() {
		return uidx;
	}
	public void setUidx(int uidx) {
		this.uidx = uidx;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public int getLevel_() {
		return level_;
	}
	public void setLevel_(int level_) {
		this.level_ = level_;
	}
	public int getDepth() {
		return depth;
	}
	public void setDepth(int depth) {
		this.depth = depth;
	}
	public int getOriginmcidx() {
		return originmcidx;
	}
	public void setOriginmcidx(int originmcidx) {
		this.originmcidx = originmcidx;
	}
	public String getDelyn() {
		return delyn;
	}
	public void setDelyn(String delyn) {
		this.delyn = delyn;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	
	

}
